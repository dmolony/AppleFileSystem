package com.bytezone.filesystem;

//-----------------------------------------------------------------------------------//
public class FileLockedException extends RuntimeException
//-----------------------------------------------------------------------------------//
{
  String message;

  // ---------------------------------------------------------------------------------//
  public FileLockedException (String string)
  // ---------------------------------------------------------------------------------//
  {
    this.message = string;
  }

  // ---------------------------------------------------------------------------------//
  @Override
  public String toString ()
  // ---------------------------------------------------------------------------------//
  {
    return message;
  }
}
