package com.bytezone.filesystem;

// -----------------------------------------------------------------------------------//
public class InvalidParentFileSystemException extends RuntimeException
// -----------------------------------------------------------------------------------//
{
  String message;

  // ---------------------------------------------------------------------------------//
  public InvalidParentFileSystemException (String string)
  // ---------------------------------------------------------------------------------//
  {
    this.message = string;
  }

  // ---------------------------------------------------------------------------------//
  @Override
  public String toString ()
  // ---------------------------------------------------------------------------------//
  {
    return message;
  }

}
